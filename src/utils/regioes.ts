const stateRegions = Object.entries(regioes).reduce((acc, [region, states]) => {
  states.forEach((state) => {
    ;(acc as { [key: string]: string })[state as keyof typeof statesNameMap] =
      region as keyof typeof regioes
  })
  return acc
}, {})

